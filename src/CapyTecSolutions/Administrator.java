package CapyTecSolutions;

public class Administrator {

    int administratorID;
    String administratorFName;
    String administratorSName;
    String administratorUsername;
    String administratorPassword;

    public Administrator() {
    }

    public int getAdministratorID() {
        return administratorID;
    }

    public void setAdministratorID(int administratorID) {
        this.administratorID = administratorID;
    }

    public String getAdministratorFName() {
        return administratorFName;
    }

    public void setAdministratorFName(String administratorFName) {
        this.administratorFName = administratorFName;
    }

    public String getAdministratorSName() {
        return administratorSName;
    }

    public void setAdministratorSName(String administratorSName) {
        this.administratorSName = administratorSName;
    }

    public String getAdministratorUsername() {
        return administratorUsername;
    }

    public void setAdministratorUsername(String administratorUsername) {
        this.administratorUsername = administratorUsername;
    }

    public String getAdministratorPassword() {
        return administratorPassword;
    }

    public void setAdministratorPassword(String administratorPassword) {
        this.administratorPassword = administratorPassword;
    }

}
