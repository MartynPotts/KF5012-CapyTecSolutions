package CapyTecSolutions;

public class Caretaker {
    int caretakerID;
    String caretakerFName;
    String caretakerSName;
    String caretakerUsername;
    String caretakerPassword;

    public Caretaker(){

    }

    public int getCaretakerID() {
        return caretakerID;
    }

    public void setCaretakerID(int caretakerID) {
        this.caretakerID = caretakerID;
    }

    public String getCaretakerFName() {
        return caretakerFName;
    }

    public void setCaretakerFName(String caretakerFName) {
        this.caretakerFName = caretakerFName;
    }

    public String getCaretakerSName() {
        return caretakerSName;
    }

    public void setCaretakerSName(String caretakerSName) {
        this.caretakerSName = caretakerSName;
    }

    public String getCaretakerUsername() {
        return caretakerUsername;
    }

    public void setCaretakerUsername(String caretakerUsername) {
        this.caretakerUsername = caretakerUsername;
    }

    public String getCaretakerPassword() {
        return caretakerPassword;
    }

    public void setCaretakerPassword(String caretakerPassword) {
        this.caretakerPassword = caretakerPassword;
    }
}
